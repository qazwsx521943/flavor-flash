import SwiftUI

struct PrimaryPreviewView: View {

	var previewImage: Image

    var body: some View {
		previewImage
			.resizable()
			.scaledToFill()
			.border(.black, width: 4)
			.clipped()
    }
}

#Preview {
	PrimaryPreviewView(previewImage: Image(systemName: "pencil"))
}
